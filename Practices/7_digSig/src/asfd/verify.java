    public byte[] loadAESK() throws IOException{
        InputStream in = new FileInputStream(dakey+nakey);
            byte[] buffer = new byte[1000];
            int bytesRead;
            byte[] aek;
        try (ByteArrayOutputStream output = new ByteArrayOutputStream()) {
            while ((bytesRead = in.read(buffer)) != -1){
                output.write(buffer, 0, bytesRead);
            }  aek = output.toByteArray();
        }
            return aek;
       
    }
    
    public byte[] AESDec(byte[] k) throws NoSuchAlgorithmException, InvalidKeySpecException, InvalidKeyException, NoSuchPaddingException, IllegalBlockSizeException, BadPaddingException, FileNotFoundException, IOException{
        SecretKeySpec ske=new SecretKeySpec(k,"AES");
        final Cipher aes = Cipher.getInstance("AES/ECB/PKCS5Padding");
        aes.init(Cipher.DECRYPT_MODE, ske);
        byte [] plaint = aes.doFinal(plaintext);
        FileOutputStream out;
        out = new FileOutputStream(dfile+"Dec"+nfile);
        out.write(plaint, 0, plaint.length);
        out.close();
        
        JOptionPane.showMessageDialog(null, "Your file have been decrypted, check it on " + dfile+"rsaEnc"+nfile,"File generated",JOptionPane.INFORMATION_MESSAGE);
        return plaint;        
    }
    
