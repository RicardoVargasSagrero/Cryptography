    public byte[] sha1(byte[] md) throws Exception{
        byte [] dig;
        MessageDigest sha= MessageDigest.getInstance("SHA1");
        dig=sha.digest(md);
        return dig;
    }
    public byte[] RSADec(byte[] pt,String sk)throws Exception{
        Cipher c = Cipher.getInstance("RSA/ECB/PKCS1Padding");
        if(sk.equals("Private"))
            c.init(Cipher.DECRYPT_MODE, s.pv);
        else
            c.init(Cipher.DECRYPT_MODE, s.pb);
        byte[] rsa=c.doFinal(pt);
        return rsa;
    }
    
